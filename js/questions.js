var imgAtual = "images/icons/svg/seta-cima.svg" // Caminho para a seta para cima
var imgAnterior = "images/icons/svg/seta-baixo.svg" // Caminho para a seta para baixo

function trocar_1() {
  let figura = document.getElementById("figura-1")
  if (figura.src.includes(imgAtual)) {
    figura.src = imgAnterior
  } else {
    figura.src = imgAtual
  }
}

